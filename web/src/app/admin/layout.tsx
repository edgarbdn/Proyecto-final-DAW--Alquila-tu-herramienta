export default function AdminLayout({ children }: { children: React.ReactNode }) {
  const pathname = usePathname();
  const router = useRouter();
  const [menuAbierto, setMenuAbierto] = useState(false);
  const [nombre, setNombre] = useState<string | null>(null);

  useEffect(() => {
    const supabase = createClient();
    supabase.auth.getUser().then(async ({ data: { user } }) => {
      if (!user) { router.push("/login"); return; }
      const { data } = await supabase.from("users").select("nombre, rol").eq("id", user.id).single();
      if (data?.rol !== "admin") { router.push("/"); return; }
      setNombre(data.nombre);
    });
  }, []);

  async function handleLogout() {
    const supabase = createClient();
    await supabase.auth.signOut();
    router.push("/");
  }

  const Sidebar = () => (
    <aside className="flex flex-col h-full w-64 shrink-0 bg-white border-r border-gray-100">
      {/* Logo */}
      <div className="px-6 py-5 border-b border-gray-100">
        <Link href="/" className="font-bold text-xl text-gray-900">
          ei<span className="text-[#F97316]">tool</span>
        </Link>
        <span className="ml-2 text-xs font-semibold text-[#F97316] bg-orange-50 px-2 py-0.5 rounded-full uppercase tracking-wider">
          Admin
        </span>
      </div>

      {/* Nav */}
      <nav className="flex-1 px-3 py-4 space-y-0.5 overflow-y-auto">
        {NAV_ITEMS.map((item) => {
          const activo = item.href === "/admin"
            ? pathname === "/admin"
            : pathname.startsWith(item.href);
          return (
            <Link
              key={item.href}
              href={item.href}
              onClick={() => setMenuAbierto(false)}
              className={`flex items-center gap-3 px-3 py-2.5 rounded-xl text-sm font-medium transition-all ${
                activo
                  ? "bg-[#F97316] text-white shadow-sm shadow-orange-200"
                  : "text-gray-500 hover:bg-orange-50 hover:text-[#F97316]"
              }`}
            >
              {item.icon}
              {item.label}
            </Link>
          );
        })}
      </nav>

      {/* Footer sidebar */}
      <div className="px-3 py-4 border-t border-gray-100 space-y-0.5">
        <Link
          href="/"
          className="flex items-center gap-3 px-3 py-2.5 rounded-xl text-sm font-medium text-gray-500 hover:bg-orange-50 hover:text-[#F97316] transition-all"
        >
          <svg className="w-5 h-5" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={1.8}>
            <path strokeLinecap="round" strokeLinejoin="round" d="M2.25 12l8.954-8.955c.44-.439 1.152-.439 1.591 0L21.75 12M4.5 9.75v10.125c0 .621.504 1.125 1.125 1.125H9.75v-4.875c0-.621.504-1.125 1.125-1.125h2.25c.621 0 1.125.504 1.125 1.125V21h4.125c.621 0 1.125-.504 1.125-1.125V9.75M8.25 21h8.25" />
          </svg>
          Ver web
        </Link>
        <button
          onClick={handleLogout}
          className="w-full flex items-center gap-3 px-3 py-2.5 rounded-xl text-sm font-medium text-gray-500 hover:bg-red-50 hover:text-red-500 transition-all"
        >
          <svg className="w-5 h-5" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={1.8}>
            <path strokeLinecap="round" strokeLinejoin="round" d="M15.75 9V5.25A2.25 2.25 0 0013.5 3h-6a2.25 2.25 0 00-2.25 2.25v13.5A2.25 2.25 0 007.5 21h6a2.25 2.25 0 002.25-2.25V15M12 9l-3 3m0 0l3 3m-3-3h12.75" />
          </svg>
          Cerrar sesión
        </button>
      </div>
    </aside>
  );

  return (
    <div className="flex h-screen bg-gray-50 overflow-hidden">
      {/* Sidebar desktop */}
      <div className="hidden md:flex flex-col h-full">
        <Sidebar />
      </div>

      {/* Sidebar móvil — overlay */}
      {menuAbierto && (
        <div className="fixed inset-0 z-50 flex md:hidden">
          <div className="flex flex-col h-full">
            <Sidebar />
          </div>
          <div className="flex-1 bg-black/50" onClick={() => setMenuAbierto(false)} />
        </div>
      )}

      {/* Contenido principal */}
      <div className="flex-1 flex flex-col overflow-hidden">
        {/* Topbar móvil */}
        <header className="md:hidden bg-white border-b border-gray-100 px-4 py-3 flex items-center justify-between shrink-0">
          <button
            onClick={() => setMenuAbierto(true)}
            className="p-2 rounded-lg text-gray-500 hover:bg-orange-50 hover:text-[#F97316] transition-colors"
          >
            <svg className="w-5 h-5" fill="none" viewBox="0 0 24 24" stroke="currentColor">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M4 6h16M4 12h16M4 18h16" />
            </svg>
          </button>
          <span className="font-bold text-gray-900">
            ei<span className="text-[#F97316]">tool</span>
            <span className="ml-1 text-xs font-semibold text-[#F97316] bg-orange-50 px-1.5 py-0.5 rounded-full">Admin</span>
          </span>
          <div className="w-9 h-9 rounded-full bg-[#F97316] flex items-center justify-center text-white text-sm font-bold">
            {nombre ? nombre[0].toUpperCase() : "A"}
          </div>
        </header>

        {/* Topbar desktop */}
        <header className="hidden md:flex bg-white border-b border-gray-100 px-6 py-3 items-center justify-between shrink-0">
          <p className="text-sm text-gray-400 font-medium">
            {NAV_ITEMS.find((i) =>
              i.href === "/admin" ? pathname === "/admin" : pathname.startsWith(i.href)
            )?.label ?? "Admin"}
          </p>
          <div className="flex items-center gap-3">
            <span className="text-sm text-gray-500">
              Hola, <span className="font-semibold text-gray-800">{nombre}</span>
            </span>
            <div className="w-8 h-8 rounded-full bg-[#F97316] flex items-center justify-center text-white text-sm font-bold">
              {nombre ? nombre[0].toUpperCase() : "A"}
            </div>
          </div>
        </header>

        {/* Página */}
        <main className="flex-1 overflow-y-auto">
          {children}
        </main>
      </div>
    </div>
  );
}
